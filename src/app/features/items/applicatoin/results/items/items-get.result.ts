import { Attachment } from "src/app/@core/shared/domain/entities/attachment";

export class ItemsGetResult {
  private constructor(
    public readonly id: string,
    public readonly name: string,
    public readonly SKUCode: string,
    public readonly manufacturer: string,
    public readonly brand: string,
    public readonly model: string,
    public readonly unit: string,
    public readonly categories: string,
    public readonly description: string,
    public readonly price: number,
    public readonly vat: number,
    public readonly stock: number,
    public readonly tags: string[],
    public readonly image?: string,
    public readonly attachments?: Attachment[],
    public readonly status?: string,
    public readonly type?: string,
    public readonly companyId: string = '',
    public readonly userId: string = '',    
    public readonly ItemNR: number = 0,    
    public readonly displayOrder: number = 0,
    public readonly isVisible: boolean = true,
    public readonly createdAt?: Date,
    public readonly updatedAt?: Date,
    public readonly deletedAt?: Date,
    public readonly createdBy?: string,
    public readonly updatedBy?: string,
    public readonly deletedBy?: string
  ) {}

  public static create(
    id: string,
    name: string,
    SKUCode: string,
    manufacturer: string,
    brand: string,
    model: string,
    unit: string,
    categories: string,
    description: string,
    price: number,
    vat: number,
    stock: number,
    tags: string[],
    image?: string,
    attachments?: Attachment[],
    status?: string,
    type?: string,    
    companyId?: string,
    userId?: string,
    ItemNR: number = 0,
    displayOrder: number = 0,
    isVisible: boolean = true,
    createdAt?: Date,
    updatedAt?: Date,
    deletedAt?: Date,
    createdBy?: string,
    updatedBy?: string,
    deletedBy?: string
  ): ItemsGetResult {
    return new ItemsGetResult(
      id,
      name,
      SKUCode,
      manufacturer,
      brand,
      model,
      unit,
      categories,
      description,
      price,
      vat,
      stock,
      tags,
      image,
      attachments,
      status,
      type,
      companyId,
      userId,
      ItemNR,
      displayOrder,
      isVisible,
      createdAt,
      updatedAt,
      deletedAt,
      createdBy,
      updatedBy,
      deletedBy
    );
  }
}
