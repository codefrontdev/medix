export class UsersInfoResult {
  private constructor(
    public readonly id: string,
    public readonly nickName: string,
    public readonly email: string,
    public readonly phoneNumber: string,
    public readonly isEmailConfirmed: boolean,
    public readonly isPhoneNumberConfirmed: boolean,
    public readonly role: string,
    public readonly gender?: string,
    public readonly accountType?: string,
    public readonly region?: string,
    public readonly city?: string,
    public readonly address?: string,
    public readonly identityType?: string,
    public readonly identityNo?: string,
    public readonly residenceNo?: string,
    public readonly dateOfBirth?: Date,
    public readonly isVerified?: boolean,
  ) { }

  public static create(
    id: string,
    nickName: string,
    email: string,
    phoneNumber: string,
    isEmailConfirmed: boolean,
    isPhoneNumberConfirmed: boolean,
    role: string,
    gender?: string,
    accountType?: string,
    region?: string,
    city?: string,
    address?: string,
    identityType?: string,
    identityNo?: string,
    residenceNo?: string,
    dateOfBirth?: Date,
    isVerified?: boolean,
  ): UsersInfoResult {
    return new UsersInfoResult(
      id,
      nickName,
      email,
      phoneNumber,
      isEmailConfirmed,
      isPhoneNumberConfirmed,
      role,
      gender,
      accountType,
      region,
      city,
      address,
      identityType,
      identityNo,
      residenceNo,
      dateOfBirth,
      isVerified,
    );
  }
}
